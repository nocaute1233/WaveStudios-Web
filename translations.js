// Get current language from localStorage or default to English
let currentLanguage = localStorage.getItem('language') || 'en';

// Function to translate the page
function translatePage() {
  const elements = document.querySelectorAll('[data-i18n]');
  elements.forEach(element => {
    const key = element.getAttribute('data-i18n');
    if (translations[currentLanguage] && translations[currentLanguage][key]) {
      element.textContent = translations[currentLanguage][key];
    }
  });

  // Update HTML lang attribute
  document.documentElement.lang = currentLanguage;

  // Update direction for RTL languages
  if (currentLanguage === 'ar') {
    document.documentElement.dir = 'rtl';
  } else {
    document.documentElement.dir = 'ltr';
  }
}

// Function to change language
function changeLanguage(lang) {
  currentLanguage = lang;
  localStorage.setItem('language', lang);
  translatePage();

  // Update active state in language selector
  document.querySelectorAll('.lang-option').forEach(option => {
    option.classList.remove('active');
    if (option.getAttribute('data-lang') === lang) {
      option.classList.add('active');
    }
  });
}

// Initialize language selector
function initLanguageSelector() {
  const langSelector = document.querySelector('.language-selector');
  if (!langSelector) return;

  const langOptions = langSelector.querySelector('.lang-options');

  // Create language options
  Object.keys(languages).forEach(langCode => {
    const option = document.createElement('div');
    option.className = 'lang-option';
    option.setAttribute('data-lang', langCode);
    option.innerHTML = `${languages[langCode].flag} <span>${languages[langCode].name}</span>`;

    if (langCode === currentLanguage) {
      option.classList.add('active');
    }

    option.addEventListener('click', () => {
      changeLanguage(langCode);
      langSelector.classList.remove('open');
    });

    langOptions.appendChild(option);
  });

  // Toggle dropdown
  const langButton = langSelector.querySelector('.lang-button');
  langButton.addEventListener('click', (e) => {
    e.stopPropagation();
    langSelector.classList.toggle('open');
  });

  // Close dropdown when clicking outside
  document.addEventListener('click', () => {
    langSelector.classList.remove('open');
  });
}

// Initialize on page load
document.addEventListener('DOMContentLoaded', () => {
  initLanguageSelector();
  translatePage();
});
